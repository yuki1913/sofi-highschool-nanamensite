/** ローカルテーブルでキーワードマッチ */
function lookupLocal(location: string): [number, number] | null {
  for (const [keyword, lat, lng] of KNOWN_LOCATIONS) {
    if (location.includes(keyword)) return [lat, lng]
  }
  return null
}

/** Nominatim フォールバック（サーバー側） */
async function geocodeNominatim(location: string): Promise<[number, number] | null> {
  try {
    const res = await fetch(
      `https://nominatim.openstreetmap.org/search?q=${encodeURIComponent(location)}&format=json&limit=1&accept-language=ja`,
      { headers: { "User-Agent": "SOFI-Nanamen-Site/1.0" } }
    )
    if (!res.ok) return null
    const data = await res.json()
    if (data[0]) return [parseFloat(data[0].lat), parseFloat(data[0].lon)]
  } catch {}
  return null
}

export async function POST(req: Request) {
  const { locations } = (await req.json()) as { locations: string[] }
  const results: Record<string, [number, number]> = {}

  const needNominatim: string[] = []

  for (const loc of locations) {
    if (!loc) continue
    const local = lookupLocal(loc)
    if (local) {
      results[loc] = local
    } else {
      needNominatim.push(loc)
    }
  }

  // ローカルで見つからなかった分だけ Nominatim に問い合わせ
  for (const loc of needNominatim) {
    const coords = await geocodeNominatim(loc)
    if (coords) results[loc] = coords
    await new Promise((r) => setTimeout(r, 1100))
  }

  return NextResponse.json(results)
}
